class Pokemon {
    name;
    type;
    health;
    pokePower;
    attacks;
    weakness;
    resistance;
    retreat;
    constructor(name, type, health, pokePower, attacks, weakness, resistance, retreat) {
        this.name = name;
        this.type = type;
        this.health = health;
        this.pokePower = pokePower;
        this.attacks = attacks;
        this.weakness = weakness;
        this.resistance = resistance;
        this.retreat = retreat;
    }


}